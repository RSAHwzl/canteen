package com.lin.canteen.bean;

//窗口分览第一个echarts图对象
public class Food {
    private int cDate;
    private int cNum;
    private String mName;

    public int getcDate() {
        return cDate;
    }

    public void setcDate(int cDate) {
        this.cDate = cDate;
    }

    public int getcNum() {
        return cNum;
    }

    public void setcNum(int cNum) {
        this.cNum = cNum;
    }

    public String getmName() {
        return mName;
    }

    public void setmName(String mName) {
        this.mName = mName;
    }

}

